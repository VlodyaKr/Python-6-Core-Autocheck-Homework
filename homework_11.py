class Iterable:
    def __init__(self, max_vectors, max_points):
        self.current_index = 0
        self.vectors = []
        self.max_vectors = max_vectors
        self.max_points = max_points

    def __next__(self):
        if self.current_index < self.max_vectors:
            self.vectors.append(Vector(Point(randrange(0, self.max_points), randrange(0, self.max_points))))
            self.current_index += 1
            return self.vectors[self.current_index - 1]
        else:
            raise StopIteration


